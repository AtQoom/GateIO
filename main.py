        obv_macd = calculate_obv_macd(SYMBOL)
        abs_val = abs(float(obv_macd * 1000))
        
        if abs_val < 5:
            leverage = Decimal("0.3")
        elif abs_val < 10:
            leverage = Decimal("0.32")
        elif abs_val < 20:
            leverage = Decimal("0.34")
        elif abs_val < 30:
            leverage = Decimal("0.36")
        elif abs_val < 40:
            leverage = Decimal("0.38")
