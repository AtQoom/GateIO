def cancel_tp_orders(symbol, side):
    """TP Ï£ºÎ¨∏ Ï∑®ÏÜå (Ïû¨ÏãúÎèÑ Î°úÏßÅ)"""
    try:
        if symbol not in tp_orders or side not in tp_orders[symbol]:
            return
        
        for tp_order in tp_orders[symbol][side][:]:
            order_id = tp_order.get("order_id")
            if not order_id:
                continue
            
            for retry in range(3):
                try:
                    api.cancel_futures_order(SETTLE, order_id)
                    tp_orders[symbol][side].remove(tp_order)
                    log_debug("‚úÖ TP Ï∑®ÏÜå", f"{symbol}_{side} ID:{order_id}")
                    break
                except Exception as e:
                    if retry < 2:
                        time.sleep(0.3)
                    else:
                        log_debug("‚ö†Ô∏è TP Ï∑®ÏÜå Ïã§Ìå®", f"ID:{order_id}")
                        try:
                            tp_orders[symbol][side].remove(tp_order)
                        except:
                            pass
    except Exception as e:
        log_debug("‚ùå TP Ï∑®ÏÜå Ïò§Î•ò", str(e), exc_info=True)

