            if grid_orders:
                log_debug("‚ö†Ô∏è Í∏∞Ï°¥ Í∑∏Î¶¨Îìú ÏûàÏùå", f"{len(grid_orders)}Í∞ú - ÏÉùÏÑ± Ï§ëÎã®")
                return
        
